/**
 * Get LGAs for a specific state
 */
export function getLGAsForState(stateCode: string) {
  const state = NIGERIA_STATES.find(s => s.code === stateCode);
  return state?.lgas || [];
}

/**
 * Get state name by code
 */
export function getStateName(stateCode: string): string {
  const state = NIGERIA_STATES.find(s => s.code === stateCode);
  return state?.name || '';
}

/**
 * Get LGA name by code
 */
export function getLGAName(stateCode: string, lgaCode: string): string {
  const state = NIGERIA_STATES.find(s => s.code === stateCode);
  const lga = state?.lgas.find(l => l.code === lgaCode);
  return lga?.name || '';
}
